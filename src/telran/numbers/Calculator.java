package telran.numbers;

public class Calculator {
	static public double sum( double op1, double op2) {
		return op1 + op2;
	}
	static public double subtract( double op1, double op2) {
		return op1 - op2;
	}
	static public double multiply( double op1, double op2) {
		return op1 * op2;
	}
	static public double devide( double op1, double op2) {
		return op1 / op2;
	}
}
